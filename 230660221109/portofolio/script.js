function toggleHobi() {
    const hobi = document.getElementById('hobi');
    hobi.style.display = hobi.style.display === 'none' || hobi.style.display === '' ? 'block' : 'none';
  }
  
  function toggleMusic() {
    const music = document.getElementById('music');
    music.style.display = music.style.display === 'none' || music.style.display === '' ? 'block' : 'none';
  }
  